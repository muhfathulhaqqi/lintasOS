    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
    Connection con = null;
        try {
    con = KoneksiDB.getKoneksi(); // âœ… ambil koneksi dari method getKoneksi()
    String sql = "SELECT * FROM username WHERE username='" + tfus.getText()
             + "' AND password='" + new String(tfps.getPassword()) + "'";
    Statement st = con.createStatement();
    ResultSet rs = st.executeQuery(sql);

    if (rs.next()) {
        if (tfus.getText().equals(rs.getString("username")) &&
            new String(tfps.getPassword()).equals(rs.getString("password"))) {

            JOptionPane.showMessageDialog(null, "berhasil login");
            this.dispose();
            ItemList hm = new ItemList();
            hm.setVisible(true);
        }
    } else {
        JOptionPane.showMessageDialog(null, "username atau password salah");
    }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, e.getMessage());
    }
    }//GEN-LAST:event_btnOKActionPerformed

