    private void tbupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbupdateActionPerformed
        try{
        String sql = "UPDATE mahasiswa SET nama=?, kelas=?, semester=? WHERE nrp=?";
        PreparedStatement pst = con.prepareStatement(sql);

        pst.setString(1, tfnama.getText());
        pst.setString(2, tfkelas.getText());
        pst.setString(3, tfsemester.getText());
        pst.setString(4, tfnrp.getText().trim());

        pst.executeUpdate();

        ShowData(null);  // <--- ini untuk refresh table
        JOptionPane.showMessageDialog(null, "Data berhasil diupdate");
    }catch(Exception e){
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_tbupdateActionPerformed

    private void tbcariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbcariActionPerformed
        try {
        con = KoneksiDB.getKoneksi();
        Statement st = con.createStatement();
        ResultSet rs = st.executeQuery("SELECT * FROM mahasiswa WHERE nrp='" + tfnrp.getText() + "'");
        
        if (rs.next()) {
            tfnama.setText(rs.getString("nama"));
            tfkelas.setText(rs.getString("kelas"));
            tfsemester.setText(rs.getString("semester"));
            JOptionPane.showMessageDialog(null, "Data ditemukan");
        } else {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "ERROR: " + e.getMessage());
    }
    }//GEN-LAST:event_tbcariActionPerformed

