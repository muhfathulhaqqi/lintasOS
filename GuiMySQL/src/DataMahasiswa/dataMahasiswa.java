    private void tbsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbsimpanActionPerformed
        nma=String.valueOf(tfnama.getText());
        nrp=String.valueOf(tfnrp.getText());
        kls=String.valueOf(tfkelas.getText());
        smt=Integer.parseInt(tfsemester.getText());
        
        try{
            sql = "INSERT INTO mahasiswa (nama,nrp,kelas,semester) VALUES "
                    + "('" + nma + "','" + nrp + "','" + kls + "'," + smt + ")";

            Statement st=con.createStatement();
            st.execute(sql);
            clear();
            ShowData(sql);
            JOptionPane.showMessageDialog(null,"Data Berhasil Disimpan");
        } catch (Exception e){
            JOptionPane.showMessageDialog(null,"Error \n Data Gagal Disimpan"+e.getMessage());
        }
    }//GEN-LAST:event_tbsimpanActionPerformed

