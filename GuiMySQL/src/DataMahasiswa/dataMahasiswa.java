private void ShowData(String sql){
    DefaultTableModel data = new DefaultTableModel();
   data.addColumn("No");
   data.addColumn("Nama");
   data.addColumn("NRP");
   data.addColumn("Kelas");
   data.addColumn("Semester");

   try{
       int i=1;
       if (con == null){
           con = KoneksiDB.getKoneksi(); // ini juga harus diberi 'con ='
       }
       Statement st=con.createStatement();
       mhswa=st.executeQuery("select * from mahasiswa");
       while (mhswa.next()){
           data.addRow(new Object[]{
               i++,
               mhswa.getString("nama"),
               mhswa.getString("nrp"),
               mhswa.getString("kelas"),
               mhswa.getString("semester")
           });
       }
       tabel1.setModel(data);
   }catch (Exception e){
       JOptionPane.showMessageDialog(null,"ERROR \n Gagal Memuat KeDatabase \n Aktifkan Database Sebelum Memulai");
   }
}

